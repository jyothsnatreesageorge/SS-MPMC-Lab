ASSUME CS:CODE,DS:DATA
DATA SEGMENT
   M1 DB 10,13,"ENTER NUMBER:$"
   M2 DB 10,13,"ODD$"
   M3 DB 10,13,"EVEN$"
DATA ENDS
PRTMSG MACRO MSG
   LEA DX,MSG
   MOV AH,09H
   INT 21H
ENDM
GETDCM MACRO
   MOV AH,01H
   INT 21H
   SUB AL,30H
ENDM
CODE SEGMENT
START:
   MOV AX,DATA
   MOV DS,AX
   PRTMSG M1
   GETDCM
   MOV AH,0
   MOV BL,02
   DIV BL
   CMP AH,0
   JE EVE
ODD:
   PRTMSG M2
   JMP EXIT
EVE:
   PRTMSG M3
EXIT:
   MOV AX,4C00H
   INT 21H
CODE ENDS
END START
