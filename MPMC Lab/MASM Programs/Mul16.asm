ASSUME CS:CODE,DS:DATA
DATA SEGMENT
   M1 DB 10,13,"ENTER FIRST NUMBER:$"
   M2 DB 10,13,"ENTER SECOND NUMBER:$"
   M3 DB 10,13,"PRODUCT:$"
   PROD DB 3 DUP(?)
DATA ENDS
PRTMSG MACRO MSG
   LEA DX,MSG
   MOV AH,09H
   INT 21H
ENDM
GETDCM MACRO
   MOV AH,01H
   INT 21H
   SUB AL,30H
ENDM
PRTDCM MACRO
   MOV DL,[SI]
   ADD DL,30H
   MOV AH,02H
   INT 21H
ENDM
CODE SEGMENT
START:
   MOV AX,DATA
   MOV DS,AX
   PRTMSG M1
   GETDCM
   MOV BH,AL
   GETDCM
   MOV BL,AL
   PRTMSG M2
   GETDCM
   MOV CH,AL
   GETDCM
   MOV CL,AL
   LEA SI,PROD
   MOV AH,00H
   MUL BL
   AAM
   MOV [SI],AL
   INC SI
   MOV [SI],AH
   MOV AH,00H
   MOV DH,AH
   MOV AL,BH
   MUL CL
   AAM
   MOV [SI],AL
   ADC DH,AH
   MOV AL,BL
   MUL CH
   AAM
   ADD [SI],AL
   ADC DH,AH
   MOV AL,BH
   MUL CH
   AAM
   ADC DH,AL
   INC SI
   MOV [SI],DH
   PRTMSG M3
   PRTDCM
   INC SI
   PRTDCM
   INC SI
   PRTDCM
   MOV AH,4CH
   INT 21H
CODE ENDS
END START
