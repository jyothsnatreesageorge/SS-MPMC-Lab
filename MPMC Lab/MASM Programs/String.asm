ASSUME CS:CODE,DS:DATA,ES:EXTRA
DATA SEGMENT
   M1 DB 10,13,"ENTER THE STRING (DELIM '):$"
   M2 DB 10,13,"VOWELS:$"
   M3 DB 10,13,"DIGITS:$"
   M4 DB 10,13,"CONSONANTS:$"
   INSTR DB "HELLO123'"
   DELIM DB "'"
   MAXLEN DB 0AH
   VC DB 00
   DC DB 00
   CC DB 00
DATA ENDS
PRTMSG MACRO MSG
   LEA DX,MSG
   MOV AH,09H
   INT 21H
ENDM
PRTCNT MACRO COUNT
   MOV DL,COUNT
   ADD DL,30H
   MOV AH,02H
   INT 21H
ENDM
EXTRA SEGMENT
   VWSTR DB "AEIOUaeiou"
   DGSTR DB "0123456789"
EXTRA ENDS
CODE SEGMENT
START:
   MOV AX,DATA
   MOV DS,AX
   MOV AX,EXTRA
   MOV ES,AX
   LEA SI,INSTR
   MOV BX,00
   MOV CL,MAXLEN
   MOV CH,00
   PRTMSG M1
   MOV AH,01H
GETC:
   INT 21H
   CMP AL,DELIM
   JE ENDGET
   INC BL
   MOV [SI],AL
   INC SI
   LOOP GETC
ENDGET:
   CLD
   LEA SI,INSTR
CHKV:
   MOV AX,[SI]
   INC SI
   MOV CL,0AH
   LEA DI,VWSTR
   REPNZ SCASB
   JNE CHKD
   INC VC
   JMP ENDC
CHKD:
   LEA DI,DGSTR
   MOV CL,0AH
   REPNZ SCASB
   JNE CHKC
   INC DC
   JMP ENDC
CHKC:
   INC CC
ENDC:
   MOV CL,BL
   DEC BX
   LOOP CHKV
   PRTMSG M2
   PRTCNT VC
   PRTMSG M3
   PRTCNT DC
   PRTMSG M4
   PRTCNT CC
   MOV AH,4CH
   INT 21H
CODE ENDS
END START
